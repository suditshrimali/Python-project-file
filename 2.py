#program to use different types of operators
a = 32
b = 13
print("a = ",a,"b = ",b)
print("Arithmetic operators : \n")
print("a + b =",a+b)
print("a - b =",a-b)
print("a * b =",a*b)
print("a / b =",a/b)
print("a ** b =",a**b)
print("a // b =",a//b)
print("a '%' b =",a%b)

print("\nRelational operators : \n")
print("Is a > b,",a>b)
print("Is a < b,",a<b)
print("Is a <= b,",a<=b)
print("Is a >= b,",a>=b)
print("Is a == b,",a==b)
print("Is a != b,",a!=b)

print("\nLogical operators : \n")
print("Is (a>b) && (a<b),",(a>b)&(a<b))
print("Is (a>b) || (a<b),",(a>b)|(a<b))

print("\nBitwise operators : \n")
print("a & b = ",a&b)
print("a | b = ",a|b)
print("a ^ b = ",a^b)
print("~a = ",~a)
print("a>>2 = ",a>>2)
print("a<<2 = ",a<<2)

print("\nIdentity operators : \n")
print("a is b = ",a is b)
print("a is not b = ",a is not b)