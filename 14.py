# Program using built-in functions on string, list, and dictionary (len(), min(), max(), sum(), sorted())

# String example
my_string = "Hello, World!"
print("Length of string:", len(my_string))
print("Minimum character in string:", min(my_string))
print("Maximum character in string:", max(my_string))

# List example
my_list = [5, 2, 9, 1, 5, 6]
print("Length of list:", len(my_list))
print("Minimum value in list:", min(my_list))
print("Maximum value in list:", max(my_list))
print("Sum of values in list:", sum(my_list))
print("Sorted list:", sorted(my_list))

# Dictionary example
my_dict = {'name': 'Tony', 'age': 30, 'city': 'New York'}
print("Length of dictionary:", len(my_dict))
print("Minimum key in dictionary:", min(my_dict))
print("Maximum key in dictionary:", max(my_dict))